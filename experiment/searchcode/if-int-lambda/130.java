        //double rnd[]={0.97,0.59,0.97,0.57,0.34,0.73,0.16,0.94,0.89,0.18,0.37,0.17,0.54,0.14,0.93,0.47,0.99,0.84,0.60,0.65,0.43,0.59,0.50,0.22,0.81,0.68};
        VectorEstado ve = new VectorEstado(semanas,rnd,Lambda,monto);
        ve.Simular();

        DecimalFormat decimales = (DecimalFormat) DecimalFormat.getInstance();
        decimales.applyPattern("0.##");

        double m[][] = ve.getMatriz();

        defaultTable = new DefaultTableModel(this.nombresColumna, 0);
        this.jTable1.setModel(defaultTable);

        Vector row;
        String str;

        double acumVentasP=0;
        double acumComprasP=0;
        double inversion=0;
        double acumDsale=0;
        double acumDentra=0;

        

        for(int i=0;i<m.length;i++)
        {
            row = new Vector();
            for(int j=0;j<m[i].length;j++)
            {
                str = new String(""+decimales.format(m[i][j]));
                //agregar nombres a eventos y demas
                if(j==1)
                {
                    if((int)m[i][j]==0)
                    {
                        str = "INICIO";
                    }
                    if((int)m[i][j]==1)
                    {
                        str = "LLEGADA";
                    }
                    if((int)m[i][j]==2)
                    {
                        str = "FinCOMPRA";
                    }
                    if((int)m[i][j]==3)
                    {
                        str = "FinVenta1";
                    }
                    if((int)m[i][j]==4)
                    {
                        str = "FinVenta2";
                    }
                }
                if(j==7)
                {
                    if((int)m[i][j]==0 && i!=0)
                    {
                        str = "RETIRADA";
                    }
                    if((int)m[i][j]==3 && i!=0)
                    {
                        str = "NO INGRESA";
                    }

                    if((int)m[i][j]==5)
                    {
                        str = "COMPRA";
                    }
                    if((int)m[i][j]==6)
                    {
                        str = "VENTA";
                    }
                  
                }
                if(j==8 || j==13 || j==18)
                {
                    if((int)m[i][j]==7)
                    {
                        str = "LIBRE";
                    }
                    if((int)m[i][j]==8)
                    {
                        str = "OCUPADO";
                    }

                }

                if(j==25 && i==semanas-1)
                {
                    acumComprasP=m[i][j];
                }

                if(j==26 && i==semanas-1)
                {
                    acumVentasP=m[i][j];
                }

                if(j==25 && i==0)
                {
                    inversion=m[i][j];
                }

                if(j==28)
                {
                    acumDsale+=m[i][j];
                }

                if(j==29)
                {
                    acumDentra+=m[i][j];
                }

                System.out.println(str);
                row.add(str);
                
            }
            this.defaultTable.addRow(row);
            
        }
        
        txtArea.setText("");
        txtArea.setText(txtArea.getText()+"\n");
        txtArea.setText(txtArea.getText()+"Ventas totales en Pesos: "+acumVentasP+"\n");
        txtArea.setText(txtArea.getText()+"Dolares Vendidos: "+acumDsale+"\n");
        txtArea.setText(txtArea.getText()+"Compras totales en Pesos : "+ acumComprasP+"\n");
        txtArea.setText(txtArea.getText()+"Dolares Comprados: "+acumDentra+"\n");
        txtArea.setText(txtArea.getText()+"Ganancias menos Inversion inicial: "+  (acumVentasP-acumComprasP)+"\n");
        txtArea.setText(txtArea.getText()+"Ganancias: "+  (acumVentasP-acumComprasP+inversion)+"\n");


        
