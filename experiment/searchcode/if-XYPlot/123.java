    public void UpdateGraficsLinFunc(double leftgr, double rightgr, double x1, double x2, double x3) {
        XYSeries series1 = new XYSeries("??????? ??????????????");
        series1.add(leftgr, 0.0);
        series1.add(x1, 0.0);
        series1.add(x2, 1.0);
        series1.add(x3, 0.0);
        series1.add(rightgr, 0.0);

        XYSeriesCollection dataset = new XYSeriesCollection();
        dataset.addSeries(series1);

        JFreeChart chart = ChartFactory.createXYLineChart(
                null,
                null,
                null,
                dataset,
                PlotOrientation.VERTICAL,
                true,
                true,
                false);

        chart.setBackgroundPaint(Color.white);
        XYPlot plot = (XYPlot) chart.getPlot();
        plot.setDomainGridlinesVisible(false);
        plot.setRangeGridlinesVisible(false);
        plot.setBackgroundPaint(Color.lightGray);
        NumberAxis x = (NumberAxis) plot.getDomainAxis();
        x.setLowerBound(leftgr);
        x.setUpperBound(rightgr);
        NumberAxis y = (NumberAxis) plot.getRangeAxis();
        y.setLowerBound(0);
        y.setUpperBound(1.02);
        XYLineAndShapeRenderer line = (XYLineAndShapeRenderer) plot.getRenderer();
        line.setSeriesPaint(0, Color.BLACK);

        ChartPanel chartPanel = new ChartPanel(chart);
        jPanel1.setLayout(new java.awt.BorderLayout());
        jPanel1.add(chartPanel, BorderLayout.CENTER);
        jPanel1.validate();
    }

