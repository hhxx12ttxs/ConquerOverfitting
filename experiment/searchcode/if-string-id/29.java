package ru.adv.db.filter;

import java.util.*;

/**
 * ????? ????? ????????.
 *
 * @version $Revision: 1.5 $
 * @see FilterCode
 */
public class FilterCodes {

    Map<String,FilterCode> codes = new TreeMap<String,FilterCode>();

    /**
     * ????????? ??? ? ????????? id.
     */
    public void put(String id, FilterCode fc) {
        codes.put(id, fc);
    }

    /**
     * ?????????? ??? ?? ????????? id (???? ?????? id ???, ??????????
     * <code>null</code>).
     */
    public FilterCode get(String id) {
        return (FilterCode) codes.get(id);
    }

    /**
     * ????????? ?????????? ?? ???????? id ? ?????.
     */
    public boolean containsKey(String id) {
        return codes.containsKey(id);
    }

    /**
     * ??????? ??? ???????? id.
     */
    public void remove(String id) {
        codes.remove(id);
    }

    /**
     * ?????????? ????? id ???????????? ? ?????.
     */
    public Set<String> keySet() {
        return codes.keySet();
    }

    /**
     * ????????????? ??? ???? ?? ???????? ?????.
     */
    public void putAll(FilterCodes fc) {
        for (String id : fc.keySet()) {
            codes.put(id, fc.get(id));
        }
    }

    /**
     * ???????? ????? ?? ?????.
     */
    public boolean isEmpty() {
        return codes.isEmpty();
    }

}

