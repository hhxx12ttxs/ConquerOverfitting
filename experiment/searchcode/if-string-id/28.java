package ru.adv.db.filter;

import java.util.*;

/**
 * ????? ????????? ????????.
 *
 * @version $Revision: 1.4 $
 * @see Filter
 */
public class FilterCollections {
    Map<String,FilterCollection> collections = new HashMap<String,FilterCollection>();

    /**
     * ?????? ????????? ? ????? ?? ?????????? id.
     */
    public void put(String id, FilterCollection fc) {
        collections.put(id, fc);
    }

    /**
     * ?????????? ????????? ?? ?????????? id (???? ?????? id ??? ?
     * ????? ?????????? <code>null</code>).
     */
    public FilterCollection get(String id) {
        return (FilterCollection) collections.get(id);
    }

    /**
     * ????????? ?????????? ?? ? ????? ???????? id.
     */
    public boolean containsKey(String id) {
        return collections.containsKey(id);
    }

    /**
     * ??????? ????????? ?? ????????? id.
     */
    public void remove(String id) {
        collections.remove(id);
    }

    /**
     * ?????????? ????? id.
     */
    public Set<String> keySet() {
        return collections.keySet();
    }

    /**
     * ????????????? ??? ????????? ?? ???????? ?????.
     */
    public void putAll(FilterCollections fc) {
        for (String id : fc.keySet()) {
            collections.put(id, fc.get(id));
        }
    }

    /**
     * ????????? ????? ?? ?????.
     */
    public boolean isEmpty() {
        return collections.isEmpty();
    }
}

