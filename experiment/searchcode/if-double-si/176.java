    private void botonOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonOKActionPerformed

        boolean exito;
        
        boolean error = ONG.gestorOfertas.eliminarOferta();

        if(!error)  //si no hay errores
        {
            exito=true;

            //ahora registro la operacion en el historial

            ONG.gestorOfertas.finEliminarOferta();

        }
        else    //si hay algun error...
        {
            exito=false;
        }
        
        if (exito) //si la operacion se ha realizado con exito y se habian introducido todos los datos
        {
            //MODIFICACION OFERTA REALIZADA
            confirmarEliminacion = new jEliminacionCorrecta();
            UI.jPrincipal.remove(confirmarEliminacion);
            UI.jPrincipal.add("ConfirmarModificacion", confirmarEliminacion);
            UI.cl.show(UI.jPrincipal, "ConfirmarModificacion");
        } else if (!exito) {
            JOptionPane.showMessageDialog(this, "No se ha podido realizar correctamente la eliminaci贸n.", "Error eliminaci贸n", JOptionPane.ERROR_MESSAGE);
        }
            
        //continuar
        
        //UI.cl.show(UI.jPrincipal, "Empleo");
    }//GEN-LAST:event_botonOKActionPerformed

